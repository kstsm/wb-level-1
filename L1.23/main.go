package main

import "fmt"

//Удалить i-ый элемент из слайса. Продемонстрируйте корректное удаление без утечки памяти.
//Подсказка: можно сдвинуть хвост слайса на место удаляемого элемента
//(copy(slice[i:], slice[i+1:])) и уменьшить длину слайса на 1.

// removeElement удаляет элемент с индексом i из слайса arr
func removeElement(arr []int, i int) []int {

	// Сдвигаем все элементы начиная с позиции i на одну позицию влево
	// (копируем arr[i+1:] в arr[i:])
	copy(arr[i:], arr[i+1:])

	// Делаем последний элемент zero-value для избежания утечки памяти
	arr[len(arr)-1] = 0

	// Возвращаем слайс без последнего элемента
	return arr[:len(arr)-1]
}

func main() {
	// Исходный слайс
	arr := []int{0, 1, 2, 3, 4, 5, 6, 7}

	// Удаляем элемент с индексом 3 (число 3)
	fmt.Println(removeElement(arr, 3)) // [0 1 2 4 5 6 7]
}
