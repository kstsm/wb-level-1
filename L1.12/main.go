package main

import "fmt"

//Имеется последовательность строк: ("cat", "cat", "dog", "cat", "tree"). Создать для неё собственное множество.
//Ожидается: получить набор уникальных слов. Для примера, множество = {"cat", "dog", "tree"}.

func main() {
	// Исходный срез строк с повторяющимися элементами
	word := []string{"cat", "cat", "dog", "cat", "tree"}

	// Срез для хранения уникальных слов
	res := make([]string, 0, len(word))

	// Map для отслеживания уже встреченных слов
	m := make(map[string]struct{})

	// Проходим по всем элементам исходного среза
	for _, v := range word {
		// Проверяем, встречалось ли слово раньше
		if _, ok := m[v]; !ok {
			// Если не встречалось, добавляем в map и в результат
			m[v] = struct{}{}
			res = append(res, v)
		}
	}

	// Выводим результат
	fmt.Println(res) // [cat dog tree]
}
