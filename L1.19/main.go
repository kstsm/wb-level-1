package main

import "fmt"

//Разработать программу, которая переворачивает подаваемую на вход строку.
//Например: при вводе строки «главрыба» вывод должен быть «абырвалг».
//Учтите, что символы могут быть в Unicode (русские буквы, emoji и пр.),
//то есть просто iterating по байтам может не подойти — нужен срез рун ([]rune).

func reverse(s string) string {
	// Преобразуем строку в срез рун (чтобы корректно обрабатывать Unicode: русские буквы, emoji и т.д.)
	r := []rune(s)

	// Два индекса: i идёт с начала строки, j — с конца
	// Пока i < j, меняем местами символы
	for i, j := 0, len(r)-1; i < j; i, j = i+1, j-1 {
		// Обмен элементов местами
		r[i], r[j] = r[j], r[i]
	}

	// Преобразуем срез рун обратно в строку и возвращаем
	return string(r)
}

func main() {
	// Исходная строка
	s := "главрыба"

	// Выводим строку до преобразования
	fmt.Println("До:", s)

	// Выводим результат после переворота строки
	fmt.Println("После:", reverse(s))
}
