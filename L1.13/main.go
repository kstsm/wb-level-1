package main

import "fmt"

//Поменять местами два числа без использования временной переменной.
//Подсказка: примените сложение/вычитание или XOR-обмен.

func main() {
	// Исходные значения чисел
	a := 5
	b := 10

	// Выводим значения до обмена
	fmt.Println("До:", a, b)

	// 1. Обмен с помощью множественного присваивания
	a, b = b, a

	// Выводим значения после обмена
	fmt.Println("После:", a, b)

	// Сбрасываем исходные значения для следующего метода
	a = 5
	b = 10

	// Выводим значения до обмена
	fmt.Println("До:", a, b)

	// 2. Обмен с помощью сложения и вычитания
	a = a + b
	b = a - b
	a = a - b

	// Выводим значения после обмена
	fmt.Println("После:", a, b)

	// Сбрасываем исходные значения для следующего метода
	a = 5  // 0101 в двоичном виде
	b = 10 // 1010 в двоичном виде

	// Выводим значения до обмена
	fmt.Println("До:", a, b)

	// 3. Обмен с помощью XOR (побитовая операция)
	a = a ^ b // a = 0101 ^ 1010 = 1111
	b = a ^ b // b = 1111 ^ 1010 = 0101 (старое значение a)
	a = a ^ b // a = 1111 ^ 0101 = 1010 (старое значение b)

	// Выводим значения после обмена
	fmt.Println("После:", a, b)
}
